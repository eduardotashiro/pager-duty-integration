import { app } from "./app";

(async () => {
  const port = process.env.PORT || 3000;
  try {
    await app.start(port);
    app.logger.info(` app is running! c: ${port}`);
  } catch (error) {
    app.logger.error(`app is not running! :c ${error} `);
    process.exit(1);
  }
})();












































